//Section 9: Algorithms: Standard Library Algorithms list
#include <algorithm> 
/**
 * The algorithms are defined in the namespace std::
 * and presented in the #<algorithm> header.
 *
 **/

 /**
  * A few most useful algos in the std:: namespace
  **/
for_each();             //Invoke function for each element
find();                 //Find first occurence of arguments
find_if();              //Find first match of predicate
count();                //Count occurence of element
count_if();             //Count matches of predicate
replace();              //Replace element of predicate
replace_if();           //Replace element that matches predicate with new value
copy();                 //Copy elements
unique_copy();          //Copy elements that are not duplicates
sort();                 //Sort elements
equal_range();          //Find all elements with equivalent values
merge();                //Merge sorted sequences 
